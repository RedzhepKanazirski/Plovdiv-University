package command;

import entities.MagicBoard;

public class BlueBikeOrder implements Command {
//--------------------------------------------------------------------------------------------------------------------------------
	public MagicBoard magicBoard;
	
//--------------------------------------------------------------------------------------------------------------------------------
    public BlueBikeOrder(MagicBoard magicBoard) 
    {
        this.magicBoard = magicBoard;
    }
 //--------------------------------------------------------------------------------------------------------------------------------	
	@Override
	public void execute() {
		magicBoard.requestToyFromDwarfs("blue bike");		
	}
//--------------------------------------------------------------------------------------------------------------------------------	
}
